type Listener = (event: {
  preventDefault: () => void;
  data: { action: { type: string } };
}) => void;

function makeNavigation() {
  const listeners: Record<string, Listener[]> = {};
  return {
    navigate: jest.fn(),
    goBack: jest.fn(),
    dispatch: jest.fn(),
    addListener: jest.fn((event: string, cb: Listener) => {
      (listeners[event] ??= []).push(cb);
      return () => {
        listeners[event] = listeners[event].filter((l) => l !== cb);
      };
    }),
    /** Simulate any pop path (swipe-back, hardware back, header back). */
    emitBeforeRemove(): { prevented: boolean; action: { type: string } } {
      let prevented = false;
      const action = { type: 'POP' };
      listeners.beforeRemove?.forEach((cb) =>
        cb({
          preventDefault: () => {
            prevented = true;
          },
          data: { action },
        })
      );
      return { prevented, action };
    },
    hasBeforeRemoveListener: () => (listeners.beforeRemove ?? []).length > 0,
    /** Simulate this screen becoming active again (e.g. back from SecuritySetup). */
    emitFocus(): void {
      listeners.focus?.forEach((cb) => cb(undefined as never));
    },
  };
}

const MOCK_WORDS = MOCK_MNEMONIC.split(' ');

/** Press the button with the given text in the most recent Alert.alert call. */
function pressAlertButton(label: string) {
  const spy = Alert.alert as unknown as jest.Mock;
  const calls = spy.mock.calls;
  const buttons = calls[calls.length - 1][2] as {
    text: string;
    onPress?: () => void;
  }[];
  const button = buttons.find((b) => b.text === label);
  expect(button).toBeDefined();
  act(() => {
    button!.onPress?.();
  });
}

